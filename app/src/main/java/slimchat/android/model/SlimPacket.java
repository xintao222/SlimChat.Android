package slimchat.android.model;

/**
 * SlimChat报文父类，子类包括消息、现场、状态等。
 * 
 * @author slimpp.io
 * 
 */
public class SlimPacket {

	/**
	 * 报文来自
	 */
	protected String from;

	/**
	 * 报文目标
	 */
	protected String to;

    /**
     * Unique ID
     */
    protected String Id;

    public SlimPacket(String from, String to) {
		this.from = from;
		this.to = to;
	}

	public String getFrom() {
		return from;
	}

	public String getTo() {
		return to;
	}

    public String getId() {
        return Id;
    }
}
